const SelectItem = React.forwardRef<
  React.ElementRef<typeof SelectPrimitive.Item>,
  React.ComponentPropsWithoutRef<typeof SelectPrimitive.Item>
>(({ className, children, ...props }, ref) => (
  <SelectPrimitive.Item
    ref={ref}
    className={cn(
      "relative flex w-full cursor-pointer select-none items-center py-2 px-2 text-base outline-none rounded-sm",
      "data-[highlighted]:bg-orange-50",
      "data-[state=checked]:bg-orange-100 data-[state=checked]:font-medium",
      "data-[highlighted]:text-orange-800 data-[state=checked]:text-orange-800",
      "hover:bg-orange-50 hover:text-orange-800",
      "focus:bg-orange-50 focus:text-orange-800",
      "transition-colors duration-150 ease-in-out",
      "data-[disabled]:opacity-50 data-[disabled]:pointer-events-none",
      className
    )}
    {...props}
  >
    <span className="absolute left-2 flex h-3.5 w-3.5 items-center justify-center">
      <SelectPrimitive.ItemIndicator>
        <Check className="h-4 w-4 text-orange-600" />
      </SelectPrimitive.ItemIndicator>
    </span>

    <SelectPrimitive.ItemText>
      <span className="pl-6">{children}</span>
    </SelectPrimitive.ItemText>
  </SelectPrimitive.Item>
))
